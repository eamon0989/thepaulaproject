const select = document.querySelector("select");
const para = document.querySelector("p");

select.addEventListener("change", setFix)

function setFix() {
    const choice = select.value;
    console.log(choice);
    

    if(choice === stupid) {
        console.log("stupid success");
        document.getElementById("stupidp").style.display = 'block';
    }else if(choice === notdone){
        console.log("not done success");
        document.getElementById("notdonep").style.display = 'block';
    }else if(choice === angry){
        console.log("angry success");
        document.getElementById("angryp").style.display = 'block';
    }else if(choice === ignore){
        console.log("ignore success");
        document.getElementById("ignorep").style.display = 'block';
    }else if(choice === noreason){
        console.log("noreason success");
        document.getElementById("noreasonp").style.display = 'block';
    }else (
        para.textContent = "Try again"
    )
}
